### numeros recorrido por lista
lista = [20, 41, 6, 18, 23]

# Recorriendo los elementos
for numero in lista:
    print(numero)

# Recorriendo los índices
for i in range(len(lista)):
    print(lista[i])

# Con while y los índices
indice = 0

while indice < len(lista):
    print(lista[indice])
    indice += 1
print (lista)    